sap.ui.define([
		"RTLtest1/controller/BaseController",
		'jquery.sap.global',
		'sap/ui/model/json/JSONModel'
	],

	function(BaseController, jQuery, JSONModel) {
		"use strict";
		return BaseController.extend("RTLtest1.controller.InvoiceD", {
			/**
			 * Called when a controller is instantiated and its View controls (if available) are already created.
			 * Can be used to modify the View before it is displayed, to bind event handlers and do other one-time initialization.
			 * @memberOf RTLtest1.view.InvoiceD
			 */
			onInit: function() {
				var oRouter = this.getRouter();
				oRouter.getRoute("details").attachMatched(this._onRouteMatched, this);

				var oView = this.getView();
				var oModel = new JSONModel("model/ponumbers.json");
				oView.setModel(oModel, "ponumbers");

			},
			_onRouteMatched: function(oEvent) {
				var oArgs, oView;
				oArgs = oEvent.getParameter("arguments");
				oView = this.getView();
				oView.bindElement({
					path: "/ZinvoiceHSet('" + oArgs.Invoiceid + "')",
					events: {
						change: this._onBindingChange.bind(this),
						dataRequested: function(oEvent) {
							oView.setBusy(true);
						},
						dataReceived: function(oEvent) {
							oView.setBusy(false);
						}
					}
				});
			},
			_onBindingChange: function(oEvent) {
					// No data for the binding
					if (!this.getView().getBindingContext()) {
						this.getRouter().getTargets().display("not_found");
					}
				}
				/**
				 * Similar to onAfterRendering, but this hook is invoked before the controller's View is re-rendered
				 * (NOT before the first rendering! onInit() is used for that one!).
				 * @memberOf RTLtest1.view.InvoiceD
				 */
				//	onBeforeRendering: function() {
				//
				//	},
				/**
				 * Called when the View has been rendered (so its HTML is part of the document). Post-rendering manipulations of the HTML could be done here.
				 * This hook is the same one that SAPUI5 controls get after being rendered.
				 * @memberOf RTLtest1.view.InvoiceD
				 */
				//	onAfterRendering: function() {
				//
				//	},
				/**
				 * Called when the Controller is destroyed. Use this one to free resources and finalize activities.
				 * @memberOf RTLtest1.view.InvoiceD
				 */
				//	onExit: function() {
				//
				//	}
				,
			/**
			 *@memberOf RTLtest1.controller.InvoiceD
			 */
			onSearch: function(event) {
				//This code was generated by the layout editor.
				var item = event.getParameter("suggestionItem");
				if (item) {
					sap.m.MessageToast.show("search for: " + item.getText());
				}

			},
			onSuggest: function(event) {
				var value = event.getParameter("suggestValue");
				var filters = [];
				if (value) {
					filters = [new sap.ui.model.Filter([
						new sap.ui.model.Filter("POId", function(sText) {
							return (sText || "").toUpperCase().indexOf(value.toUpperCase()) > -1;
						}),
						new sap.ui.model.Filter("PODescription", function(sDes) {
							return (sDes || "").toUpperCase().indexOf(value.toUpperCase()) > -1;
						})
					], false)];
				}
				var oSearchPO = this.getView().byId("searchpo");
				oSearchPO.getBinding("suggestionItems").filter(filters, false);
				oSearchPO.suggest();
			}
		});
	});